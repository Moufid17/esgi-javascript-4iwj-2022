// Valeur permettant de représenter L'absence de valeur
console.log(null);

// Une valeur non-définie
console.log(undefined);

// Une chaîne de caractères (délimitée avec des guillemets)
console.log("String");

// Une chaîne de caractères (délimitée avec des apostrophes)
console.log('String');

// Un booléen (vrai)
console.log(true);

// Un booléen (faux)
console.log(false);

// Un nombre entier
console.log(123);

// Un nombre décimal
console.log(1.23);

// Un nombre avec notation exponentielle
console.log(10e8);

// L'infini (positif)
console.log(Infinity);

// L'infini (négatif)
console.log(-Infinity);

// Not A Number (une erreur de type dans un calcul)
console.log(NaN);

// Une manière d'obtenir NaN
console.log(1 + undefined);

// Obtenir le type d'une valeur scalaire
console.log(typeof 123);
